package Operators;

public class Arithematic {
public static void main(String[] args) {
	int a=20;
	int b=7;
	System.out.println(a+b);      //20+7=27
	System.out.println(a-b);      //20-7=13
	System.out.println(a*b);      //20*7=140
	System.out.println(a/b);      //20/7=2
	System.out.println(a%b);      //20%7=6
}
}
