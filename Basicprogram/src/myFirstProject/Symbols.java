package myFirstProject;

public class Symbols {
	public static void main(String[]args) {
        
		
		// '//'->this is used to comment out a particular statement.
		// '()'->this is called as a method signature.
		
		System.out.println("Maharashtra is divided in 6 divisions.");
		
		// ';'->it is used to end a particular sentence.
		
		System.out.println("Godavari "+"Krishna are the "+"main river in "+"Maharashta");
		
		// '+'->this is used to join the two statements.
		
		System.out.println("1]Mumbai\n2]Pune\n3]Aurangabad n etc.\nare the industrial cities in MH.");
		
		// '\n'->this means to move to a new line at this specific point.
		
}
}