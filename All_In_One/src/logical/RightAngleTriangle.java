public class RightAngleTriangle {
public static void main(String[] args) {
	for(int a=1; a<=5; a++)
       {
		      for(int b=1; b<=a; b++)      //a=3  b=a=2 b=3
		       {
		           	System.out.print("* ");
		       }
		     System.out.println();
        }
	
}
}
